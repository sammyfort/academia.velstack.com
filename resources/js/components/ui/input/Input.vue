<script setup lang="ts">
import { useVModel } from '@vueuse/core'
import { cn } from '@/lib/utils'
import { useAttrs } from 'vue'

const props = defineProps<{
    defaultValue?: string | number
    modelValue?: string | number
    class?: string
}>()

const emits = defineEmits<{
    (e: 'update:modelValue', value: string | number): void
}>()

const modelValue = useVModel(props, 'modelValue', emits, {
    passive: true,
    defaultValue: props.defaultValue,
})

const attrs = useAttrs()
</script>

<template>
    <input
        v-model="modelValue"
        v-bind="attrs"
        :class="cn(
      'file:text-foreground placeholder:text-muted-foreground selection:bg-foreground selection:text-secondary-foreground dark:bg-input/30 border-input flex h-9 w-full min-w-0 rounded-md border bg-transparent px-3 py-1 text-base shadow-xs transition-[color,box-shadow] outline-none file:inline-flex file:h-7 file:border-0 file:bg-transparent file:text-sm file:font-medium disabled:pointer-events-none disabled:cursor-not-allowed disabled:opacity-50 md:text-sm',
      'focus-visible:border-ring focus-visible:ring-ring/5 focus-visible:ring-[3px]',
      'aria-invalid:ring-destructive/20 dark:aria-invalid:ring-destructive/40 aria-invalid:border-destructive',
      props.class
    )"
    />
</template>
